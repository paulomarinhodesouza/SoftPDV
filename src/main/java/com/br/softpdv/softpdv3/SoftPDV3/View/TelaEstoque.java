    private void atualizarQuantidade(){
        ListaProdutos listaProdutos = new ListaProdutos();
        codigo = txtCodigo.getText();
        Produtos produtoAtual = listaProdutos.buscarProdutoCodigo(codigo, codigo);
        
        if(produtoAtual != null){
            quantidade = produtoAtual.getQuantidade();
            lbQuantidade.setText(quantidade);
            txtQuantidade.requestFocusInWindow();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Produto nÃ£o existente", "Erro", JOptionPane.ERROR_MESSAGE);
            lbQuantidade.setText("0");
        }               
    }
    
    private void novaQuantidade(){
        ListaProdutos listaProdutos = new ListaProdutos();
        String novaQuantidade = txtQuantidade.getText();
        Produtos produtoAtual = listaProdutos.buscarProdutoCodigo(codigo, codigo);
        
        if(produtoAtual != null){
            ProdutosDAO.atualizarQuantidade(produtoAtual, novaQuantidade);
            lbNovaQuantidade.setText(novaQuantidade);
            JOptionPane.showMessageDialog(rootPane, "Quantidade alterada de " + quantidade + " para " + novaQuantidade, "Sucesso", JOptionPane.PLAIN_MESSAGE);
        }
    }
